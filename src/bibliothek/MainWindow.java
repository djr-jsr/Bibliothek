        jPanel21.setVisible(flag);
        DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
        tb.setRowCount(0);
        int counter = 0;

        try {
            Statement stmt = null;

            Connection con = DriverManager.getConnection(url, user, password);
            System.out.println("Success");
            stmt = con.createStatement();
            String add = "SELECT * FROM books";
            ResultSet rs = stmt.executeQuery(add);
            while (rs.next()) {
                boolean flagDate = false;
                String name = rs.getString("name");
                String ISBN = rs.getString("ISBN");
                Date date = new Date();
                byte[] buf = rs.getBytes("copyDetails");
                ObjectInputStream o = new ObjectInputStream(new ByteArrayInputStream(buf));
                ArrayList<SubBook> copyDetails = (ArrayList<SubBook>) o.readObject();
                System.out.println(name + " " + ISBN);
                Iterator itr = copyDetails.iterator();
                while (itr.hasNext()) {
                    SubBook sb = (SubBook) itr.next();
                    Iterator iter = sb.getIssuedMembers().iterator();
                    while (iter.hasNext()) {
                        flagDate = true;
                        IssueDetails_book iss = (IssueDetails_book) iter.next();
                        SimpleDateFormat sd = new SimpleDateFormat("dd/MM/yyyy");
                        Date dd = sd.parse(iss.getIssueDate());
                        if (dd.compareTo(date) < 0) {
                            date = dd;
                        }
                    }
                }
                counter++;
                String date1;
                if (flagDate) {
                    SimpleDateFormat sdf = new SimpleDateFormat("dd-MMM-yyyy");
                    date1 = sdf.format(date);
                } else {
                    date1 = "Never Issued";
                }
                Object[] obj = new Object[]{counter, name, ISBN, date1};
                tb.addRow(obj);

            }
        } catch (Exception e) {

        }

